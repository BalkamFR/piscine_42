#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char **argv)
{
	ft_putstr(argv[0]);
	ft_putstr("\n");
	(void)argc;
	return (0);
}
